// Q.6: Write a program to check whether an alphabet is a vowel or consonant.

void main() {
  String check = 'b';

  if (check == 'a' ||
      check == 'e' ||
      check == 'i' ||
      check == 'o' ||
      check == 'u') {
    print(' This alphabet is vowel');
  } else {
    print('This alphabet is consonant');
  }

  print(check);
}
